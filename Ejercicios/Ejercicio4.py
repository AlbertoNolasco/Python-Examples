"""
Ejercicio 4:
Pedir dos numeros al usuario y hacer las operaciones básicas de una calculadora y mostrar por pantalla
(Sumar, Restar, Multiplicar y dividir)

"""

number_one = int(input("Ingrese el primer numero por favor: "))
number_two = int(input("Ingrese el segundo numero por favor: "))

print(f"La suma de los numeros es: {number_one + number_two}")
print(f"La resta de los numeros es: {number_one - number_two}")
print(f"La multiplicación de los numeros es: {number_one * number_two}")
print(f"La divición de los numeros es: {number_one / number_two}")

