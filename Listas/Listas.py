"""
Listas (Arrays)

Son colecciones o conjuntos de datos/valores, bajo un unico nombre. 
para acceder a esos valores podemos usar un indice numerico.

"""

pelicula = "Batman"

print(pelicula)

# creacion de listas o definir listas.
peliculas = ["Batman","Spiderman", "Los vengadores"]

print(peliculas)


# o 
cantantes = list(('2pac','Drake', 'Lopez'))

print(cantantes)

years = list(range(2020,2050))

print(years)

variada = ["Alberto", 29, 1.70, True]
print(variada)
